                DOMWarning("expected incoming " + string(name) +
                                   " link to be an object-property connection, ignoring",
                        &element);
                return null;
            }

            if (is_object_property_conn && propNameOut) {
                // note: this is ok, the return value of PropertyValue() is guaranteed to
                // remain valid and unchanged as long as the document exists.
                *propNameOut = con.PropertyName().c_str();
            }

            FBXObject* ob = con.SourceObject();
            if (!ob) {
                DOMWarning("failed to read source object for incoming " + string(name) +
                                   " link, ignoring",
                        &element);
                return null;
            }

            return dynamic_cast<T*>(ob);
        }


        // fetch a property table and the corresponding property template
        public static PropertyTable GetPropertyTable(FBXDocument doc,
                string templateName,
                FBXElement element,
                FBXScope sc,
                bool no_warn = false) {
            FBXElement* Properties70 = sc["Properties70"];
            (PropertyTable)templateProps = (PropertyTable)(static_cast<PropertyTable*>(null));

            if (templateName.length()) {
                Dictionary<string, PropertyTable>::const_iterator it = doc.Templates().find(templateName);
                if (it != doc.Templates().end()) {
                    templateProps = (*it).second;
                }
            }

            if (!Properties70 || !Properties70.Compound()) {
                if (!no_warn) {
                    DOMWarning("property table (Properties70) not found", &element);
                }
                if (templateProps) {
                    return templateProps;
                }
                else {
                    return std::make_shared<PropertyTable>();
                }
            }
            return std::make_shared<PropertyTable>(*Properties70, templateProps);
        }


