        /** Decode a single Base64-encoded character.
    *
    *  @param ch Character to decode (from base64 to binary).
    *  @return decoded byte value*/
        byte DecodeBase64(char ch) {
            //auto idx = static_cast<byte>(ch);
            var idx = (byte)ch;
            if (idx > 127)
                return 255;
            return base64DecodeTable[idx];
        }

