        static aiSkeleton? createAiSkeleton(SkeletonBoneContainer sbc) {
            if (sbc.MeshArray.Count == 0 || sbc.SkeletonBoneToMeshLookup.Count == 0) {
                return null;
            }

            var skeleton = new aiSkeleton();
            foreach (var mesh in sbc.MeshArray) {
                if (sbc.SkeletonBoneToMeshLookup.TryGetValue(mesh, out var skeletonBones)) {
                    if (skeletonBones == null) {
                        continue;
                    }

                    skeleton.mNumBones = (uint)skeletonBones.Count;
                    skeleton.mBones = new aiSkeletonBone[skeleton.mNumBones];
                    int index = 0;
                    foreach (var bone in skeletonBones) {
                        skeleton.mBones[index++] = bone;
                    }
                }
            }

            return skeleton;
        }

