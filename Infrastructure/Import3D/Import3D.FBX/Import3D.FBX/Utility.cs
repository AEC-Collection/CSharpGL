        // Lengthy utility function to read and resolve a FBX vertex data array - that is, the
        // output is in polygon vertex order. This logic is used for reading normals, UVs, colors,
        // tangents ..
        template<typename T>
void ResolveVertexDataArray(List<T> &data_out, FBXScope source,
        string &MappingInformationType,
        string &ReferenceInformationType,
        char* dataElementName,
        char* indexDataElementName,
        int vertex_count,
        List<uint> &mapping_counts,
        List<uint> &mapping_offsets,
        List<uint> &mappings) {
            bool isDirect = ReferenceInformationType == "Direct";
            bool isIndexToDirect = ReferenceInformationType == "IndexToDirect";
            bool hasDataElement = HasElement(source, dataElementName);
            bool hasIndexDataElement = HasElement(source, indexDataElementName);

            // fall-back to direct data if there is no index data element
            if (isIndexToDirect && !hasIndexDataElement) {
                isDirect = true;
                isIndexToDirect = false;
            }

            // handle permutations of Mapping and Reference type - it would be nice to
            // deal with this more elegantly and with less redundancy, but right
            // now it seems unavoidable.
            if (MappingInformationType == "ByVertice" && isDirect) {
                if (!hasDataElement) {
                    FBXImporter::LogWarn("missing data element: ", dataElementName);
                    return;
                }
                List<T> tempData;
                ParseVectorDataArray(tempData, GetRequiredElement(source, dataElementName));

                if (tempData.Count != mapping_offsets.Count) {
                    FBXImporter::LogError("length of input data unexpected for ByVertice mapping: ",
                            tempData.Count, ", expected ", mapping_offsets.Count);
                    return;
                }

                data_out.resize(vertex_count);
                for (int i = 0, e = tempData.Count; i < e; ++i) {
                    uint istart = mapping_offsets[i], iend = istart + mapping_counts[i];
                    for (uint j = istart; j < iend; ++j) {
                        data_out[mappings[j]] = tempData[i];
                    }
                }
            }
            else if (MappingInformationType == "ByVertice" && isIndexToDirect) {
                List<T> tempData;
                if (!hasDataElement || !hasIndexDataElement) {
                    if (!hasDataElement)
                        FBXImporter::LogWarn("missing data element: ", dataElementName);
                    if (!hasIndexDataElement)
                        FBXImporter::LogWarn("missing index data element: ", indexDataElementName);
                    return;
                }

                ParseVectorDataArray(tempData, GetRequiredElement(source, dataElementName));

                List<int> uvIndices;
                ParseVectorDataArray(uvIndices, GetRequiredElement(source, indexDataElementName));

                if (uvIndices.Count != mapping_offsets.Count) {
                    FBXImporter::LogError("length of input data unexpected for ByVertice mapping: ",
                            uvIndices.Count, ", expected ", mapping_offsets.Count);
                    return;
                }

                data_out.resize(vertex_count);

                for (int i = 0, e = uvIndices.Count; i < e; ++i) {

                    uint istart = mapping_offsets[i], iend = istart + mapping_counts[i];
                    for (uint j = istart; j < iend; ++j) {
                        if (static_cast<int>(uvIndices[i]) >= tempData.Count) {
                            DOMError("index out of range", &GetRequiredElement(source, indexDataElementName));
                        }
                        data_out[mappings[j]] = tempData[uvIndices[i]];
                    }
                }
            }
            else if (MappingInformationType == "ByPolygonVertex" && isDirect) {
                if (!hasDataElement) {
                    FBXImporter::LogWarn("missing data element: ", dataElementName);
                    return;
                }

                List<T> tempData;
                ParseVectorDataArray(tempData, GetRequiredElement(source, dataElementName));

                if (tempData.Count != vertex_count) {
                    FBXImporter::LogError("length of input data unexpected for ByPolygon mapping: ",
                            tempData.Count, ", expected ", vertex_count);
                    return;
                }

                data_out.swap(tempData);
            }
            else if (MappingInformationType == "ByPolygonVertex" && isIndexToDirect) {
                List<T> tempData;
                if (!hasDataElement || !hasIndexDataElement) {
                    if (!hasDataElement)
                        FBXImporter::LogWarn("missing data element: ", dataElementName);
                    if (!hasIndexDataElement)
                        FBXImporter::LogWarn("missing index data element: ", indexDataElementName);
                    return;
                }
                ParseVectorDataArray(tempData, GetRequiredElement(source, dataElementName));

                List<int> uvIndices;
                ParseVectorDataArray(uvIndices, GetRequiredElement(source, indexDataElementName));

                if (uvIndices.Count > vertex_count) {
                    FBXImporter::LogWarn("trimming length of input array for ByPolygonVertex mapping: ",
                            uvIndices.Count, ", expected ", vertex_count);
                    uvIndices.resize(vertex_count);
                }

                if (uvIndices.Count != vertex_count) {
                    FBXImporter::LogError("length of input data unexpected for ByPolygonVertex mapping: ",
                            uvIndices.Count, ", expected ", vertex_count);
                    return;
                }

                data_out.resize(vertex_count);

                T empty;
                uint next = 0;
                for (int i : uvIndices) {
                    if (-1 == i) {
                        data_out[next++] = empty;
                        continue;
                    }
                    if (static_cast<int>(i) >= tempData.Count) {
                        DOMError("index out of range", &GetRequiredElement(source, indexDataElementName));
                    }

                    data_out[next++] = tempData[i];
                }
            }
            else {
                FBXImporter::LogError("ignoring vertex data channel, access type not implemented: ",
                        MappingInformationType, ",", ReferenceInformationType);
            }
        }

