        static string to_base64_string = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
        /** Encode bytes in base64-encoding
      *
      *  @param data Binary data to encode.
      *  @param inLength Number of bytes to encode.
      *  @return base64-encoded string*/
        char EncodeBase64(char _byte) {
            return to_base64_string[(int)_byte];
        }

        /** Encodes a block of 4 bytes to base64 encoding
         *
         *  @param bytes Bytes to encode.
         *  @param out_string String to write encoded values to.
         *  @param string_pos Position in out_string.*/
        void EncodeByteBlock(char* bytes, StringBuilder out_string, int string_pos) {
            char b0 = (char)((bytes[0] & 0xFC) >> 2);
            char b1 = (char)((bytes[0] & 0x03) << 4 | ((bytes[1] & 0xF0) >> 4));
            char b2 = (char)((bytes[1] & 0x0F) << 2 | ((bytes[2] & 0xC0) >> 6));
            char b3 = (char)((bytes[2] & 0x3F));

            out_string[string_pos + 0] = EncodeBase64(b0);
            out_string[string_pos + 1] = EncodeBase64(b1);
            out_string[string_pos + 2] = EncodeBase64(b2);
            out_string[string_pos + 3] = EncodeBase64(b3);
        }


        StringBuilder EncodeBase64(char* data, int length) {
            // calculate extra bytes needed to get a multiple of 3
            int extraBytes = 3 - length % 3;

            // number of base64 bytes
            int encodedBytes = 4 * (length + extraBytes) / 3;

            //string encoded_string(encodedBytes, '=');
            var encoded_string = new StringBuilder(encodedBytes);
            for (int i = 0; i < encodedBytes; i++) { encoded_string[i] = '='; }

            // read blocks of 3 bytes
            for (int ib3 = 0; ib3 < length / 3; ib3++) {
                int iByte = ib3 * 3;
                int iEncodedByte = ib3 * 4;
                char* currData = &data[iByte];

                EncodeByteBlock(currData, encoded_string, iEncodedByte);
            }

            // if size of data is not a multiple of 3, also encode the final bytes (and add zeros where needed)
            if (extraBytes > 0) {
                char[] finalBytes = { '\0', '\0', '\0', '\0' };
                //memcpy(finalBytes, &data[length - length % 3], length % 3);
                //Array.Copy(finalBytes, 0, data, length - length % 3, length % 3);
                for (int i = 0; i < length % 3; i++) {
                    data[length - length % 3 + i] = '\0';
                }

                int iEncodedByte = encodedBytes - 4;
                fixed (char* pBytes = finalBytes) {
                    EncodeByteBlock(pBytes, encoded_string, iEncodedByte);
                }

                // add '=' at the end
                for (int i = 0; i < 4 * extraBytes / 3; i++)
                    encoded_string[encodedBytes - i - 1] = '=';
            }
            return encoded_string;
        }


